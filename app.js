// ==== TEMPLATE SELECTION ====
function selectTemplate(element, type) {
  // Find parent container
  const container = element.closest('.template-selector');
  if(!container) return;
  // Remove active from all siblings
  container.querySelectorAll('.tmpl-option').forEach(el => el.classList.remove('active'));
  // Add active to clicked
  element.classList.add('active');
}

function switchTab(type) {
  // Tabs
  const tabFresher = document.getElementById('tab-fresher');
  const tabExp = document.getElementById('tab-exp');
  
  if(tabFresher && tabExp) {
    tabFresher.classList.remove('active');
    tabExp.classList.remove('active');
    document.getElementById(`tab-${type}`).classList.add('active');
  }
  
  // Grids
  const fresherGrid = document.getElementById('fresherTemplates');
  const expGrid = document.getElementById('expTemplates');
  
  if(fresherGrid && expGrid) {
    if (type === 'fresher') {
      fresherGrid.classList.remove('hidden');
      expGrid.classList.add('hidden');
    } else {
      expGrid.classList.remove('hidden');
      fresherGrid.classList.add('hidden');
    }
  }
}

// ==== GENERATE & SUCCESS LOGIC ====
function generateResume(type) {
  // Close builder modal
  if(type === 'fresher') closeModal('fresherModal');
  if(type === 'exp') closeModal('expModal');
  
  // Open success modal
  const successModal = document.getElementById('successModal');
  if(successModal) {
    successModal.classList.add('active');
  }
}

