// ==== MODAL LOGIC ====
function openModule(type) {
  if (type === 'fresher') {
    document.getElementById('fresherModal').classList.add('active');
    resetForm('fresher');
  } else if (type === 'experienced') {
    document.getElementById('expModal').classList.add('active');
    resetForm('exp');
  }
}

function closeModal(modalId) {
  document.getElementById(modalId).classList.remove('active');
}

// ==== MULTISTEP FORM LOGIC ====
let fStep = 1;
const fMaxSteps = 5;

let eStep = 1;
const eMaxSteps = 5;

function updateProgress(type, step, maxSteps) {
  const progressFill = document.getElementById(`${type}Progress`);
  const progressLabel = document.getElementById(`${type}ProgressLabel`);
  if (!progressFill || !progressLabel) return;
  const percent = (step / maxSteps) * 100;
  progressFill.style.width = `${percent}%`;
  progressLabel.textContent = `Step ${step} of ${maxSteps}`;
}

function resetForm(type) {
  if (type === 'fresher') {
    fStep = 1;
    showStep('fresher', fStep);
  } else {
    eStep = 1;
    showStep('exp', eStep);
  }
}

function showStep(type, step) {
  const maxSteps = type === 'fresher' ? fMaxSteps : eMaxSteps;
  const prefix = type === 'fresher' ? 'f' : 'e';
  
  // Hide all steps
  for (let i = 1; i <= maxSteps; i++) {
    const el = document.getElementById(`${prefix}Step${i}`);
    if (el) {
      el.classList.remove('active');
    }
  }
  
  // Show target
  const targetEl = document.getElementById(`${prefix}Step${step}`);
  if (targetEl) {
    targetEl.classList.add('active');
  }
  
  // Update UI
  updateProgress(type, step, maxSteps);
  updateButtons(type, step, maxSteps);
}

function updateButtons(type, step, maxSteps) {
  const backBtn = document.getElementById(`${type}Back`);
  const nextBtn = document.getElementById(`${type}Next`);
  const generateBtn = document.getElementById(`${type}Generate`);
  if(!backBtn || !nextBtn || !generateBtn) return;
  
  if (step === 1) {
    backBtn.style.display = 'none';
  } else {
    backBtn.style.display = 'block';
  }
  
  if (step === maxSteps) {
    nextBtn.style.display = 'none';
    generateBtn.style.display = 'block';
  } else {
    nextBtn.style.display = 'block';
    generateBtn.style.display = 'none';
  }
}

function nextStep(type) {
  if (type === 'fresher') {
    if (fStep < fMaxSteps) {
      fStep++;
      showStep('fresher', fStep);
    }
  } else if (type === 'exp') {
    if (eStep < eMaxSteps) {
      eStep++;
      showStep('exp', eStep);
    }
  }
}

function prevStep(type) {
  if (type === 'fresher') {
    if (fStep > 1) {
      fStep--;
      showStep('fresher', fStep);
    }
  } else if (type === 'exp') {
    if (eStep > 1) {
      eStep--;
      showStep('exp', eStep);
    }
  }
}

